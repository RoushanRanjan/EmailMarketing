package com.nacre.emailmarketing.dto;


public class UserOperationDto {
	private String productownername;
	private Integer operationtypeproductcount;
	private String servicename;
	private Integer operationtypeservicecount;
	public String getProductownername() {
		return productownername;
	}
	public void setProductownername(String productownername) {
		this.productownername = productownername;
	}
	public Integer getOperationtypeproductcount() {
		return operationtypeproductcount;
	}
	public void setOperationtypeproductcount(Integer operationtypeproductcount) {
		this.operationtypeproductcount = operationtypeproductcount;
	}
	public String getServicename() {
		return servicename;
	}
	public void setServicename(String servicename) {
		this.servicename = servicename;
	}
	public Integer getOperationtypeservicecount() {
		return operationtypeservicecount;
	}
	public void setOperationtypeservicecount(Integer operationtypeservicecount) {
		this.operationtypeservicecount = operationtypeservicecount;
	}
	@Override
	public String toString() {
		return "UserOperationDto [productownername=" + productownername + ", operationtypeproductcount="
				+ operationtypeproductcount + ", servicename=" + servicename + ", operationtypeservicecount="
				+ operationtypeservicecount + "]";
	}
	

}
